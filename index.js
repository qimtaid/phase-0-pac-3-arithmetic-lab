function add(a, b){
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function divide(a, b) {
    return a/b;
}

function multiply(a, b) {
    return a*b;
}

function increment(n) {
    return n + 1;
}

function decrement(n) {
    return n - 1;
}

// Function to parse integer
function makeInt(n) {
    return parseInt(n, 10);
  }
  
  // Function to parse floating point number
  function preserveDecimal(n) {
    return parseFloat(n);
  }
  